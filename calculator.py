import math
# square root
print('The square root of 900 is',math.sqrt(900))
# absolute value as float
print('The absolute value of -10 as a float is',math.fabs(-10))
# ceiling
print('The ceiling of 9.2 is',math.ceil(9.2))
# floor
print('The floor of 9.2 is',math.floor(9.2))
# trig functions
print('The sin of 45 is',math.sin(45))
print('The cos of 45 is',math.cos(45))
print('The tan of 45 is',math.tan(45))
# e^x and logs
print('e^1.5 is',math.exp(1.5))
print('The log, base e, of 3.5 is',math.log(3.5))
print('The log, base 10, of 100 is',math.log10(100))
# power x raised to y
print('2 raised to the 7th power is',math.pow(2.0, 7.0))
# remainder of x/y as a floating point number
print('The remainder of 9.8/4 is',math.fmod(9.8, 4.0))
